package package01;

/**
 * This class is a subclass of Weapon. Note: Since it is a subclass you do not need to write public String name and Public int damage.
 * All you need to write is the name of each variable (i.e. name and damage).
 */

public class Knife extends Weapon {

    //Constructor which contains the weapons stats
    public Knife(){

        name = "Knife";
        damage = 20;

    }
}
